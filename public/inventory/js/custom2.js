document.addEventListener('DOMContentLoaded', function () {
    const toggleBtn = document.getElementById('toggleSearchBtn');
    const searchInput = document.getElementById('sidebarSearch');
    const wrapper = document.getElementById('searchWrapper');

    toggleBtn.addEventListener('click', function (e) {
        e.stopPropagation(); // Avoid triggering document click close rules immediately
        
        // Toggle the expanded layout configuration wrapper
        wrapper.classList.toggle('expanded');
        
        if (wrapper.classList.contains('expanded')) {
            // Unhide field variables and assign layout space parameters smoothly
            searchInput.style.display = 'block';
            searchInput.style.width = '100%';
            searchInput.style.padding = '0.25rem 0.5rem';
            searchInput.focus(); // Snap focus directly onto the selector field input
        } else {
            // Collapse back downwards safely if clicked again while empty
            if(!searchInput.value) {
                searchInput.style.display = 'none';
                searchInput.style.width = '0';
                searchInput.style.padding = '0';
            }
        }
    });

    // Auto-collapse rule: Closes search field container frame if clicked anywhere else outside the sidebar node
    document.addEventListener('click', function (e) {
        if (!wrapper.contains(e.target) && !searchInput.value) {
            wrapper.classList.remove('expanded');
            searchInput.style.display = 'none';
            searchInput.style.width = '0';
            searchInput.style.padding = '0';
        }
    });
});